class MonstersValley2 {
    public:
        int minimumPrice(vector <int> dread, vector <int> price) {
            int sz = dread.size();
            long long int ans = 999999999;
            for(int s=0; s<(1<<sz); s++){
                //cur state
                bool ok = true;
                long long int gold = 0;
                long long int cur_d = 0;
                for(int i=0; i<sz; i++){
                    if( ((s>>i)&1)==1 ){
                        gold += price[i];
                        cur_d += dread[i];
                    }else{
                        if(cur_d<dread[i]){
                            ok = false;
                            break;
                        }
                    }
                }
                if(ok){
                    ans = min(ans, gold);
                }
            }
            return ans;
        }
};

