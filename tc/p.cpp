class PikachuEasy {
    public:
        string check(string word) {
            if(word.size()<=1){
                return "NO";
            }
            int ok=1;
            for(int i=0; i<word.size()-1; ){
                if(word[i]=='k' && word[i+1]=='a'){
                    i+=2;
                    continue;
                }
                if(word[i]=='p' && word[i+1]=='i'){
                    i+=2;
                    continue;
                }
                if(i<word.size()-2){
                    if(word[i]=='c' && word[i+1]=='h' && word[i+2]=='u'){
                        i+=3;
                        continue;
                    }
                }
                ok=0;
                break;
            }
            if(ok){
                return "YES";
            }else{
                return "NO";
            }
        }
};

