class EllysPairs {
    public:
        int getDifference(vector <int> knowledge) {
            sort(knowledge.begin(), knowledge.end());
            int sz = knowledge.size();
            vector<int> tmp;
            int front = 0, rear = sz-1;
            while(front<rear){
                tmp.push_back( knowledge[front] + knowledge[rear]);
                front++,rear--;
            }
            sort(tmp.begin(), tmp.end());
            return abs(tmp[0]-tmp[tmp.size()-1]);
        }
};

