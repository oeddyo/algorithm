class MagicalStringDiv1 {
    public:
        int getLongest(string S) {
            int sz = S.size();
            int ans = 0;

            for(int i=0; i<sz; i++){
                int l=0, r=0;
                for(int j=0; j<=i; j++){
                    if(S[j]=='>')l++;
                }
                for(int j=i+1; j<sz; j++){
                    if(S[j]=='<')r++;
                }
                cout<<l<<" "<<r<<endl;
                int tmp = min(l, r)*2;
                if(tmp>ans)ans = tmp;
            }
            return ans; 
        }
};
