class MicroStrings {
    public:
        string to_str(int a){
            string tmp = "";
            while(a>0){
                tmp += (a%10)+'0';
                a/=10;
            }
            return string(tmp.rbegin(), tmp.rend());
        }

        string makeMicroString(int A, int D) {
            string ans = "";
            while(A>=0){
                if(A==0){
                    ans += '0';
                    break;
                }
                cout<<to_str(A)<<endl;
                ans += to_str(A);
                A -= D;
            }
            return ans;
        }
};

